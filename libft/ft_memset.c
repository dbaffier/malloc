#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*b2;
	size_t	i;

	i = 0;
	b2 = (char *)b;
	while (i < len)
	{
		b2[i] = c;
		i++;
	}
	return ((void *)b2);
}
